public void reset()
{
    File s=new File("school.txt");
    File t=new File("trainer.txt");
    File m=new File("module.txt");
    File l=new File("level.txt");
    File d=new File("department.txt");
    int msg=JOptionPane.showConfirmDialog(null, "You are about to reset all information.\n Are you sure to proceed?", "Reset", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
    if(msg==JOptionPane.YES_OPTION)
    {
       
      if(  s.delete() && t.delete() && m.delete() && l.delete() && d.delete())
      {
          JOptionPane.showMessageDialog(null,"Information reset");
          dispose();
      }
      else
      {
         JOptionPane.showMessageDialog(null,"Information not reset"); 
      }
        
    }
}
