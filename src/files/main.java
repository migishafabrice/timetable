    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        // TODO add your handling code here:
        PrintWriter writer;
        PrintWriter dp;
        
            try {
                writer=new PrintWriter("school.txt","UTF-8");
                writer.println("SCHOOL INFORMATION");
                writer.println("");
                writer.println("Name:\t"+txtname.getText());
                writer.println("Manager:\t"+txtmanager.getText());
                writer.println("Head teacher:\t"+txtvmanager.getText());
                writer.println("Telephone:\t"+txttel.getText());
                writer.println("Email:\t"+txtmail.getText());
                writer.println("P.O Box:\t"+txtbox.getText());
                writer.println("Province:\t"+txtpro.getText());
                writer.println("District:\t"+txtdi.getText());
                writer.println("Sector:\t"+txtse.getText());
                writer.println("");
                if(depart.size()>=1)
                {
                    dp=new PrintWriter("department.txt","UTF-8");
                     dp.println("DEPARTMENTS");
                     dp.println("");
                    int s=0;
                    while(s<depart.size())
                    {
                    dp.println(depart.get(s));
                    s++;
                    }
                    dp.close();
                }
               lbinfo.setText("School and department information added");
               lbinfo.setForeground(Color.blue);
               
                writer.close();
                
            } catch (FileNotFoundException | UnsupportedEncodingException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        
        
        
    }//GEN-LAST:event_btsaveActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n;
        String dep;
        DefaultTableModel d;
        d=new DefaultTableModel();
        Object[] col=new Object[1];
        col[0]="Departments";
        d.setColumnIdentifiers(col);
        Object r[]=new Object[1];
        depart=new ArrayList();
        n=Integer.valueOf(txtnber.getText());
        for(int i=1;i<=n;i++)
        {
        dep=JOptionPane.showInputDialog(null, "Department:\n "+String.valueOf(i), "Enter department number", JOptionPane.INFORMATION_MESSAGE);
        depart.add(dep);
        r[0]=dep;
        d.addRow(r);
        }
        tbdep.setModel(d);
    }//GEN-LAST:event_jButton1ActionPerformed

